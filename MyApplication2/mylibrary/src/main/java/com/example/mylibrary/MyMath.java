public class MyMath {
    public static int Plus(int a , int b){
        return a*b;
    }
    public static int Minus(int a , int b){
        return a-b;
    }
    public static int Multiplier(int a , int b){
        return a*b;
    }
    public static int Divi(int a , int b){
        return a/b;
    }
    public static int Func(int a , int b ,int c){
        return a+b+c;
    }

}


